import os
from fpdf import FPDF

# Color Palette
DARK_BLUE = (26, 37, 48)
TEAL = (0, 180, 216)
LIGHT_GRAY = (244, 246, 247)
WHITE = (255, 255, 255)
TEXT_DARK = (40, 40, 40)
TEXT_LIGHT = (100, 100, 100)

class DesignerPDF(FPDF):
    def __init__(self):
        super().__init__()
        # Add custom designer fonts
        self.add_font("Roboto", "", "Roboto-Regular.ttf")
        self.add_font("Roboto", "B", "Roboto-Bold.ttf")
        self.add_font("Roboto", "I", "Roboto-Italic.ttf")
        self.set_auto_page_break(auto=True, margin=20)
        self.custom_title = ""

    def header(self):
        # Draw a beautiful top geometric header
        self.set_fill_color(*DARK_BLUE)
        self.rect(0, 0, 210, 35, "F")
        self.set_fill_color(*TEAL)
        self.rect(0, 33, 210, 2, "F") # Accent line
        
        self.set_y(15)
        self.set_font("Roboto", "B", 24)
        self.set_text_color(*WHITE)
        self.cell(0, 0, "TORTUGA CYBERSECURITY", align="C")
        self.ln(12)

    def footer(self):
        self.set_y(-20)
        self.set_fill_color(*TEAL)
        self.rect(0, 287, 210, 10, "F") # Bottom accent
        self.set_font("Roboto", "", 9)
        self.set_text_color(150, 150, 150)
        self.cell(0, 10, f"Page {self.page_no()} | Generated by TORTUGA Security", align="C")

    def chapter_title(self, title):
        self.ln(10)
        # Background block for title
        self.set_fill_color(*LIGHT_GRAY)
        self.rect(10, self.get_y(), 190, 12, "F")
        self.set_fill_color(*TEAL)
        self.rect(10, self.get_y(), 3, 12, "F") # Left accent line
        
        self.set_y(self.get_y() + 3)
        self.set_x(18)
        self.set_font("Roboto", "B", 14)
        self.set_text_color(*DARK_BLUE)
        self.cell(0, 6, title)
        self.ln(6)

    def chapter_body(self, body):
        self.set_font("Roboto", "", 11)
        self.set_text_color(*TEXT_DARK)
        
        # We replace some markdown-like syntax for bolding
        # We will just write HTML for the body to make bolding easier
        # But fpdf2 write_html with custom fonts needs the font to be registered.
        self.write_html(body)
        self.ln(5)

