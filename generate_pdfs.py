def create_pdf(filename, title, content_sections):
    pdf = DesignerPDF()
    pdf.add_page()
    
    # Main Title Header (Below the geometric band)
    pdf.set_y(45)
    pdf.set_font("Roboto", "B", 20)
    pdf.set_text_color(*DARK_BLUE)
    pdf.cell(0, 10, title, align="C")
    pdf.ln()
    
    pdf.set_font("Roboto", "", 12)
    pdf.set_text_color(*TEXT_LIGHT)
    subtitle = "A Guide to Protecting Yourself Online" if "Phishing" in title and "Guide" in title else "Una Guía para Protegerte en Línea"
    pdf.cell(0, 6, subtitle, align="C")
    pdf.ln()
    pdf.ln(5)

    for section in content_sections:
        pdf.chapter_title(section["title"])
        # Replace newlines in body to avoid weird HTML parsing issues, ensure it's a tight string
        body = section["body"].replace('\n', ' ')
        
        # Apply specific CSS via write_html if possible, or just raw HTML
        # fpdf2 allows some styling
        pdf.chapter_body(body)
        
    os.makedirs("docs", exist_ok=True)
    pdf.output(filename)

